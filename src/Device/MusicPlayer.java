package Device;

public class MusicPlayer {
    public void play() {
        System.out.println("Music player is playing your favorite playlist");
    }
    public void stop() {
        System.out.println("Music player stopped");
    }
    public void increaseVolume() {
        System.out.println("Volume increased by 5 units");
    }
    public void decreaseVolume() {
        System.out.println("Volume decreased by 5 units");
    }
}
